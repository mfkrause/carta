/// Whether `scheme` is a registered URI scheme recognized for autolinking. `scheme` must already be
/// lowercased; the table holds only lowercase names.
pub(crate) fn is_scheme(scheme: &str) -> bool {
    SCHEMES.binary_search(&scheme).is_ok()
}
